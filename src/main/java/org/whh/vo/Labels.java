package org.whh.vo;

public class Labels
{
	private int time;

	private String tagid;

	private String label;

	public void setTime(int time)
	{
		this.time = time;
	}

	public int getTime()
	{
		return this.time;
	}

	public void setTagid(String tagid)
	{
		this.tagid = tagid;
	}

	public String getTagid()
	{
		return this.tagid;
	}

	public void setLabel(String label)
	{
		this.label = label;
	}

	public String getLabel()
	{
		return this.label;
	}
}
